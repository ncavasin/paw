            <p>La <strong>Ecografía</strong>, también conocida como Diagnóstico por Imágenes con Ultrasonido, utiliza un transductor para generar ondas de sonido y producir imágenes de las estructuras internas del cuerpo.</p>

            <h3>Ecografía Doppler, ¿qué es?</h3>
            <p>Por otra parte, el ultrasonido o <strong>Ecografía Doppler</strong> consiste en una técnica especial de ultrasonido que le permite al médico ver y evaluar la circulación de la sangre a través de arterias y venas en el abdomen, brazos, piernas,
                cuello y/o cerebro (en infantes y en niños), o dentro de varios órganos del cuerpo tales como el hígado y los riñones.</p>
            <p>Cabe destacar que la <b>Ecografía Doppler</b> se trata de un proceso no invasivo que permite calcular el flujo de la sangre dentro de los vasos sanguíneos, haciendo rebotar ondas sonoras de alta frecuencia en los glóbulos rojos circulantes.</p>

            <h3>¿Para qué sirve una Ecografía?</h3>
            <p>Existen dos tipos de ecografías principales: Por un lado está la <b>Ecografía de Embarazo</b>, la cual sirve para analizar el feto y brinda información precisa acerca de la salud general del bebé durante su etapa de gestación.</p>
            <p>El otro tipo de ecografía corresponde a la <b>Ecografía Médica Diagnóstica</b>, la cual se utiliza para analizar las partes internas del cuerpo, tales como: Corazón, vasos sanguíneos, riñones, vejiga, hígado y órganos reproductores femeninos.</p>
            <p>A través de la realización de una Ecografía durante el embarazo es posible: Confirmarlo, averiguar la edad gestacional (el tiempo que lleva un embarazo), dar cuenta si se trata de un embarazo múltiple, comprobar el tamaño y posición del feto,
                entre otros factores.</p>
            <p>Por su parte, la <b>Ecografía Médica Diagnóstica</b> sirve para: Dar cuenta si hay algún inconveniente con la estructura del corazón, detectar si la sangre fluye a un nivel normal, analizar la tiroides con el objetivo de detectar la presencia
                de cáncer o incluso tumores no cancerosos y también permite detectar problemas en el abdomen y riñones.</p>

            <h3>¿Para qué se hace la Ecografía Doppler?</h3>
            <p>La Ecografía Doppler es de gran utilidad para la detección de distintos tipos de afecciones, tales como: arterias bloqueadas, coágulos sanguíneos, insuficiencia venosa, disminución de la circulación sanguínea hacia las piernas, arterias ensanchadas,
                estrechamiento de arterias y enfermedades cardíacas congénitas.</p>
            <p>Este estudio también permite ayudar al médico en la detección de lesiones en las arterias, como así también supervisar determinados tratamientos que apliquen tanto para las venas como para las arterias.</p>
